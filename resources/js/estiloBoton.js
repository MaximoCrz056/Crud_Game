function cambiarEstado(estado){
    var disponibleBtn = document.getElementById('disponible');
    var ocupadoBtn = document.getElementById('ocupado');
    if ( estado == 'disponible'){
        disponibleBtn.style.backgroundColor ='green';
        ocupadoBtn.style.backgroundColor='black';
    } else if (estado == 'ocupado'){
        disponibleBtn.style.backgroundColor='black';
        ocupadoBtn.style.backgroundColor = 'red';
    }

}
